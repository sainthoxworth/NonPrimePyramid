public class NonPrimeNumber {
    public int x;
    public int y;
    public int value;

    public NonPrimeNumber(int xx, int yy, int value){
        this.x = xx;
        this.y = yy;
        this.value = value;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
